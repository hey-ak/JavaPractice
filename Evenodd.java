public class Evenodd {
    static void Evenodd(int num) {
        if (num % 2 == 0) {
            System.out.println("Even");

        }
        else {
            System.out.println("Odd");
        }
    }
    public static void main(String[] args) {
        int n = 5;
        Evenodd(n);

    }
    
}
